import os
import sys

"""
The os module provides a portable way to interact with the operating system, 
including file/directory operations, process management, and environment variables.
"""

# os.listdir(path) # List files/directories in a given path.
# os.mkdir(path) / os.makedirs(path): Create directories (single or nested).
# os.remove(path) / os.rmdir(path): Delete files or empty directories.
# os.rename(src, dst): Rename/move files or directories.
# os.path submodule: Path manipulations

# os.environ.get(key) / os.environ[key]: Access environment variables.

# os.path.join(a, b): Join path components OS-agnostically.
# os.path.exists(path): Check if a path exists.
# os.path.isdir(path) / os.path.isfile(path): Check path type.
# os.path.abspath(path): Convert to absolute path.

# os.getcwd(): Get the current working directory.
# os.chdir(path): Change the working directory.




